namespace CapstoneUWP.Model.Repo_Model
{
    public class Student
    {
        /// Gets or sets the identifier.
        /// </summary>
        /// <value>
        /// The identifier.
        /// </value>
        public int Id { get; set; }

        /// <summary>
        /// Gets or sets the first name.
        /// </summary>
        /// <value>
        /// The first name.
        /// </value>
        public string FirstName { get; set; }

        /// <summary>
        /// Gets or sets the last name.
        /// </summary>
        /// <value>
        /// The last name.
        /// </value>
        public string LastName { get; set; }

        /// <summary>
        /// Gets or sets the email.
        /// </summary>
        /// <value>
        /// The email.
        /// </value>
        public string Email { get; set; }

        /// <summary>
        /// Gets or sets the phone.
        /// </summary>
        /// <value>
        /// The phone.
        /// </value>
        public string Phone { get; set; }

        /// <summary>
        /// Gets or sets the degree program.
        /// </summary>
        /// <value>
        /// The degree program.
        /// </value>
        public string DegreeProgram { get; set; }

        /// <summary>
        /// Gets or sets the degree progress.
        /// </summary>
        /// <value>
        /// The degree progress.
        /// </value>
        public string DegreeProgress { get; set; }

        /// <summary>
        /// Gets or sets the gpa.
        /// </summary>
        /// <value>
        /// The gpa.
        /// </value>
        public string GPA { get; set; }
    }
}
